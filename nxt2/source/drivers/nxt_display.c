static uint8 display_tick = 0;
static uint8 display_auto_update = 1;

static uint8 display_x;
static uint8 display_y;

void nxt_display_update(void)
{
    display_tick = 0;
    nxt_lcd_update();
}

void nxt_display_set_auto_update(uint8 mode)
{
    /* Enable/disable automatic refresh of the display. */
    display_auto_update = mode;
}

void nxt_display_force_update(void)
{
    /* Force a display update even if interrupts are disabled. */
    nxt_lcd_force_update();
}

void nxt_display_clear(uint8 update)
{
    memset(display_buffer, 0, DISPLAY_WIDTH * DISPLAY_DEPTH);

    if (update)
        nxt_display_update();
}

void nxt_display_goto_xy(uint8 x, uint8 y)
{
    display_x = x;
    display_y = y;
}

void nxt_display_char(int c)
{
    int i;
    uint8 *b;
    const uint8 *f;

    if (c >= 0 && c < N_CHARS &&
        display_x >= 0 && display_x < DISPLAY_CHAR_WIDTH &&
        display_y >= 0 && display_y < DISPLAY_CHAR_DEPTH)
    {
        b = &display_buffer[display_y][display_x * CELL_WIDTH];
        f = font[c];

        for (i = 0; i < FONT_WIDTH; i++)
        {
            *b = *f;
            b++;
            f++;
        }
    }
}

void nxt_display_string(const char *str)
{
    while (*str)
    {
        if (*str != '\n')
        {
            nxt_display_char(*str);
            display_x++;
        }
        else
        {
            display_x = 0;
            display_y++;
        }
        str++;
    }
}

void nxt_display_hex(uint32 val, uint32 places)
{
    char x[9];
    char *p = &x[8];
    int p_count = 0;

    *p = 0;

    if (places > 8)
        places = 8;

    while (val)
    {
        p--;
        p_count++;
        *p = "0123456789ABCDEF"[val & 0x0f];
        val >>= 4;
    }

    while (p_count < places)
    {
        p--;
        p_count++;
        *p = '0';
    }

    nxt_display_string(p);
}

static void nxt_display_unsigned_worker(uint32 val, uint32 places, uint32 sign)
{
    char x[12]; /* enough for 10 digits + sign + NULL */
    char *p = &x[11];
    int p_count = 0;

    *p = 0;

    if (places > 11)
        places = 11;

    while (val)
    {
        p--;
        p_count++;
        *p = (val % 10) + '0';
        val /= 10;
    }

    if (!p_count)
    {
        p--;
        p_count++;
        *p = '0';
    }

    if (sign)
    {
        p--;
        p_count++;
        *p = '-';
    }

    while (p_count < places)
    {
        p--;
        p_count++;
        *p = ' ';
    }

    nxt_display_string(p);
}

void nxt_display_unsigned(uint32 val, uint32 places)
{
    nxt_display_unsigned_worker(val, places, 0);
}

void nxt_display_signed(sint32 val, uint32 places)
{
    nxt_display_unsigned_worker((val < 0) ? -val : val, places, (val < 0));
}

void nxt_display_bitmap_copy(const uint8 *data, uint32 width, uint32 depth, uint32 x,
                             uint32 y)
{
    uint32 i;
    uint32 j;
    uint32 dy;
    uint32 dx;

    for (i = 0; i < depth; i++)
    {
        for (j = 0; j < width; j++)
        {
            dx = x + j;
            dy = y + i;
            if (dx < DISPLAY_WIDTH && dy < DISPLAY_DEPTH)
            {
                display_buffer[y + i][x + j] = data[width * i + j];
            }
        }
    }
}

uint8 *nxt_display_get_buffer(void)
{
    return (uint8 *)display_buffer;
}

void nxt_display_init(void)
{
    /* Wait for LCD to stabilize. */
    systick_wait_ms(1000);

    /* Clear the screen. */
    nxt_display_clear(0);

    display_auto_update = 1;
}
